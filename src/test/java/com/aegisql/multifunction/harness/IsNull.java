package com.aegisql.multifunction.harness;

public class IsNull extends Exception {
    public IsNull() {
    }

    public IsNull(String message) {
        super(message);
    }

    public IsNull(String message, Throwable cause) {
        super(message, cause);
    }

    public IsNull(Throwable cause) {
        super(cause);
    }

    public IsNull(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
